import http from 'k6/http';
import { check, sleep, group } from 'k6';
import { Rate, Trend } from 'k6/metrics';

// Load test for meetjyou-backend local dev instance.
// Requires: SPRING_PROFILES_ACTIVE=dev,db,secrets, dev.bypass.enabled=true (default in application-dev.yml)
// Run: k6 run loadtest/mixed-scenario.js
// Override target: k6 run -e BASE_URL=http://localhost:8080 loadtest/mixed-scenario.js

const BASE_URL = __ENV.BASE_URL || 'http://localhost:8080';

const errorRate = new Rate('errors');
const postCreateTrend = new Trend('post_create_duration');
const planCreateTrend = new Trend('plan_create_duration');

export const options = {
  scenarios: {
    ramping_load: {
      executor: 'ramping-vus',
      startVUs: 0,
      stages: [
        { duration: '30s', target: 20 },
        { duration: '1m', target: 20 },
        { duration: '30s', target: 50 },
        { duration: '1m', target: 50 },
        { duration: '30s', target: 100 },
        { duration: '1m', target: 100 },
        { duration: '30s', target: 0 },
      ],
    },
  },
  thresholds: {
    http_req_duration: ['p(95)<800'],
    errors: ['rate<0.05'],
  },
};

// Each VU registers its own dev user once via setup-per-VU pattern (k6 runs setup() once globally,
// so we mint a user lazily on first iteration per VU using __VU as a unique seed).
function registerVU() {
  const unique = `${Date.now()}${Math.floor(Math.random() * 100000)}`;
  const email = `loadtest-vu${__VU}-${unique}@local.test`;
  // nickname is capped at 8 chars server-side; pack VU+iter+random into base36.
  const seed = __VU * 1e6 + __ITER * 1000 + Math.floor(Math.random() * 1000);
  const nickname = `u${seed.toString(36)}`.slice(0, 8);
  const res = http.post(
    `${BASE_URL}/api/v1/dev/auth/register`,
    JSON.stringify({ email, nickname }),
    { headers: { 'Content-Type': 'application/json' } },
  );
  check(res, { 'register status 201': (r) => r.status === 201 });
  if (res.status !== 201) {
    errorRate.add(1);
    return null;
  }
  const body = res.json();
  return { uuid: body.uuid, accessToken: body.accessToken };
}

let vuSession = null;

export default function () {
  if (!vuSession) {
    vuSession = registerVU();
    if (!vuSession) {
      sleep(1);
      return;
    }
  }
  const authHeaders = {
    headers: {
      'Content-Type': 'application/json',
      Authorization: `Bearer ${vuSession.accessToken}`,
    },
  };

  group('read-heavy browsing', () => {
    const listPosts = http.get(`${BASE_URL}/api/v1/posts?page=0&size=20`, authHeaders);
    check(listPosts, { 'list posts 200': (r) => r.status === 200 }) || errorRate.add(1);

    const listRooms = http.get(`${BASE_URL}/api/v1/chat/rooms`, authHeaders);
    check(listRooms, { 'list chat rooms 200': (r) => r.status === 200 }) || errorRate.add(1);

    // Fresh dev-registered users have no Preference row yet, so /users/me/profile 404s
    // with PREFERENCE_NOT_FOUND until onboarding is completed; use a preference-agnostic
    // endpoint instead so this doesn't register as a false-positive server error.
    const myPosts = http.get(`${BASE_URL}/api/v1/users/me/posts?page=0&size=10`, authHeaders);
    check(myPosts, { 'my posts 200': (r) => r.status === 200 }) || errorRate.add(1);
  });

  sleep(Math.random() * 1.5);

  group('write path: create post', () => {
    const payload = JSON.stringify({
      title: `Load test trip ${__VU}-${__ITER}`,
      content: 'Generated by k6 load test',
      isInstant: false,
      itinStart: '2026-08-01T00:00:00Z',
      itinFinish: '2026-08-05T00:00:00Z',
      location: '제주',
      capacity: 4,
    });
    const res = http.post(`${BASE_URL}/api/v1/posts`, payload, authHeaders);
    postCreateTrend.add(res.timings.duration);
    check(res, { 'create post 200': (r) => r.status === 200 }) || errorRate.add(1);
  });

  sleep(Math.random() * 1.5);

  group('write path: create plan', () => {
    const payload = JSON.stringify({
      title: `Plan ${__VU}-${__ITER}`,
      itinStart: '2026-08-01T00:00:00Z',
      itinFinish: '2026-08-05T00:00:00Z',
      location: '제주',
      centerLat: 33.4996,
      centerLng: 126.5312,
      markers: [],
    });
    const res = http.post(`${BASE_URL}/api/v1/plans`, payload, authHeaders);
    planCreateTrend.add(res.timings.duration);
    check(res, { 'create plan 200': (r) => r.status === 200 }) || errorRate.add(1);
  });

  sleep(Math.random() * 2);
}
